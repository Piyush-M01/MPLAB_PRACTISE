IO0DIR EQU 0XE0028008
IO0CLR EQU 0XE002800C
IO0SET EQU 0XE0028004
IO1DIR EQU 0XE0028018
IO1PIN EQU 0XE0028010

	AREA LED_SWITCH,CODE,READONLY
	EXPORT __main
__main
	ENTRY
		LDR R0,=IO0DIR
		LDR R1,=IO1DIR
		LDR R2,=IO1PIN
		LDR R3,=IO0SET
		LDR R4,=IO0CLR
		
		LDR R5,=0X00FF0000
		STR R5,[R3]
		
REPEAT	LDR R6,[R2]
		AND R6,#0X00FF0000
		CMP R6,#0X00FF0000
		BEQ REPEAT
		STR R5,[R3]
		EOR R6,#0X00FF0000
		STR R6,[R4]
		B REPEAT
		END
		
		