PINSEL1 EQU 0XE002C004
DACR	EQU 0XE006C000

	AREA STAIRCASE,CODE,READONLY
	EXPORT __main
__main
	ENTRY
		LDR R0,=PINSEL1
		LDR R1,=DACR
		
		LDR R2,=0X00080000
		STR R2,[R0]
		
		LDR R5,=102
		
BACK	MOV R4,R6,LSL #6
		STR R4,[R1]
		ADD R3,R3,#1
		CMP R3,R5
		BNE BACK
		BEQ REDUCE
REDUCE	ADD R5,R5,R5
		MOV R6,R5
		BEQ BACK
		END
		