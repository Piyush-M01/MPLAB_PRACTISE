IO0DIR EQU 0XE0028008
IO0CLR EQU 0XE002800C
IO0SET EQU 0XE0028004

	AREA EXT_DAC,CODE,READONLY
	EXPORT __main
__main
	ENTRY
		LDR R0,=IO0DIR
		LDR R1,=IO0SET
		LDR R2,=IO0CLR
		LDR R3,=0xff0000
	
BACK	STR R3,[R2]
		BL DELAY
		STR R3,[R1]
		BL DELAY
		B BACK
DELAY
		LDR R5,=0X2FFF
UP		SUBS R5,R5,#1
		BNE UP
		BX LR
		END