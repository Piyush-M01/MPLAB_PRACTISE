	AREA ASCEND,CODE,READONLY
	EXPORT __main
__main
	ENTRY
		LDR R0,=NUM
		LDR R2,=ELE
		LDR R5,=5
LOOP0	LDR R1,[R0],#4
		STR R1,[R2],#4
		SUBS R5,R5,#1
		CMP R5,#0
		BNE LOOP0
		
		LDR R0,=ELE
		LDR R6,=6

LOOP	MOV R1,R0
		SUB R6,R6,#1
		MOV R5,R6
		LDR R2,[R0],#4
		

LOOP2	LDR R3,[R1,#4]!
		SUBS R5,R5,#1
		CMP R5,#0
		BEQ LOOP
		CMP R2,R3
		BGT SWAP
		BLT LOOP2
		
SWAP	MOV R4,R3
		MOV R3,R2
		MOV R2,R4
		STR R3,[R1]
		STR R2,[R0,#-4]
		B LOOP2
NUM		DCD	0X5,0X4,0X3,0X2,0X1	
	AREA MEM,DATA,READWRITE

ELE		SPACE	5
		END