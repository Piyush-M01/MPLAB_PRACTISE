IO0SET EQU 0XE0028004
IO0CLR EQU 0XE002800C
IO0DIR	EQU 0XE0028008

	AREA ringCounter, CODE, READONLY
	EXPORT __main
__main
	ENTRY
			LDR R1,=IO0DIR
			LDR R2,=0X00FF0000
			STR R2,[R1]
			
			;TURN ON 23RD LED
			LDR R1,=IO0SET
			LDR R2,=0X00800000
			STR R2,[R1]
			
			LDR R0,=IO0CLR
			b RIGHT
			
			;SHIFT RIGHT UNTIL 0.16 NOT EQUALS 1, OTHERWISE 0.23 =1 
RIGHT
			STR R2,[R0]
			MOV R2,R2,LSR #1
			STR R2,[R1]
			LDR R3,=0X2fffff
			BL DELAY1
			CMP R2,#0X00008000
			BNE RIGHT
			BEQ RESET
			
RESET	
			STR R2,[R0]
			LDR R2,=0X00800000
			STR R2,[R1]
			LDR R3,=0X2fffff
			BL DELAY1
			b RIGHT

DELAY1 
			SUBS R3,R3,#1
			BNE DELAY1
			bx lr
			END


			
			