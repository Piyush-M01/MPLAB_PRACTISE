PINSEL1 EQU 0XE002C004
DACR	EQU 0XE006C000

	AREA TRIANGLE,CODE,READONLY
	EXPORT __main
__main
	ENTRY
		LDR R0,=PINSEL1
		LDR R1,=DACR
		
		LDR R2,=0X00080000
		STR R2,[R0]
		
		LDR R5,=1023
		
BACK	MOV R4,R3,LSL #6
		STR R4,[R1]
		ADD R3,R3,#1
		CMP R3,R5
		BNE BACK
		BEQ REDUCE
REDUCE	MOV R4,R3,LSL #6
		STR R4,[R1]
		SUB R3,R3,#1
		CMP R3,#0
		BNE REDUCE
		BEQ BACK
		END
		